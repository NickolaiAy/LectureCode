package org.example.Week9_API;

public class Movie {
    private String name;
    private int stars;
    private boolean watched;
    Movie(String name, int stars, boolean watched) {
        this.name = name;
        this.stars = stars;
        this.watched = watched;
    }

    public String getName() {
        return getName();
    }

    public String getStars() {
        return getStars();
    }

    public boolean isWatched() {
        return isWatched();
    }
}
